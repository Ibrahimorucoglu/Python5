object Hello extends App {

  // boolean datatype
  val bool1 : Boolean = true
  val bool2 : Boolean = false
  println("Boolean datatype: ",bool1)


  // Integers
  val integer1 : Byte = -77
  val integer2 : Short = 847
  val integer3 : Int = 344473
  val integer4 : Long = 75727825
  println("Integer datatype: ",integer1)

  // Floating points
  val floating1 : Float = 454.32f
  val floating2 : Double = 454.32
  println("Floating point datatype: ",floating1)

  // String
  val string1 : String = "Qarabag is Azerbaijan"
  val character1 : Char = 'I'
  println("String datatype: ",string1)
  println("Character: ",character1)



}
